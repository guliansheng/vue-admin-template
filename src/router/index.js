/**
 * Note: sub-menu only appear when route children.length >= 1
 * Detail see: https://panjiachen.github.io/vue-element-admin-site/guide/essentials/router-and-nav.html
 *
 * hidden: true                   if set true, item will not show in the sidebar(default is false) 如果设置为true，则item将不会显示在侧边栏中(默认为false)
 * alwaysShow: true               if set true, will always show the root menu 如果设置为true，将始终显示根菜单
 *                                if not set alwaysShow, when item has more than one children route,
 *                                it will becomes nested mode, otherwise not show the root menu
 * redirect: noRedirect           if set noRedirect will no redirect in the breadcrumb 如果设置noRedirect将不会在面包屑中重定向
 * name:'router-name'             the name is used by <keep-alive> (must set!!!) <keep-alive>(必须设置!!)使用该名称。
 * meta : {
    roles: ['admin','editor']    control the page roles (you can set multiple roles) 控制页面角色(可以设置多个角色)
    title: 'title'               the name show in sidebar and breadcrumb (recommend set) 名称显示在侧边栏和面包屑
    icon: 'svg-name'/'el-icon-x' the icon show in the sidebar 图标显示在侧边栏
    breadcrumb: false            if set false, the item will hidden in breadcrumb(default is true) 如果设置为false，该项目将在breadcrumb中隐藏(默认为true)
    activeMenu: '/example/list'  if set path, the sidebar will highlight the path you set 如果设置path，工具条将突出显示设置的path
  }
 */

